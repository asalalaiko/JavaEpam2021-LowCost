package by.asalalaiko.domain;

import by.asalalaiko.repo.WithId;

public class City implements WithId {

    private Long id;
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
